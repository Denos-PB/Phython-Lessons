import math

# a) The value of sqrt(2) in fixed-point format with three decimal places
sqrt_2 = math.sqrt(2)
print(f"a) {sqrt_2:.3f}")

# б) Value (2.5)**1.6 in normalized format with four decimal places
value = 2.5**1.6
print(f"б) {value:.4e}")

# в) The sum of pi and e in fixed-point format with two digits after the point
pi_e_sum = math.pi + math.e
print(f"в) {pi_e_sum:.2f}")