#перший рядок
print(type(int(4.1)),int(4.1))
print(type(int(-4.1)),int(-4.1))
print(type(int(4.5)),int(4.5))
print("-"*10)
#другий рядок
print(type(float(14)),float(14))
print(type(complex(13)),complex(13))
print("-"*10)
#третій рядок
print(type(float(bool(1.5))),float(bool(1.5)))
print(type(float(int(1.56))),float(int(1.56)))
print(type(bool(-0.0)),bool(-0.0))
print("-"*10)
